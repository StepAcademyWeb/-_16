let left = prompt("введите первое число");
let right = prompt("введите второе чисо");
let sum = left + right;
let sub = left - right;
let div = left / right;
let multi = left * right;
alert(`${left} + ${right} = ${sum}`);
alert(`${left} - ${right} = ${sub}`);
alert(`${left} / ${right} = ${div}`);
alert(`${left} * ${right} = ${multi}`);